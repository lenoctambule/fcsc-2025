INT_BITS = 8
def leftRotate(n, d):
    return (n << d)|(n >> (INT_BITS - d))

def rightRotate(n, d):
    return (n >> d) | (n << (INT_BITS - d)) & 0xFF

for i in range(len(ct)) :
    c = ct[i] ^ (i * 3 + 0x1F)
    c = rightRotate(c, 3)
    print(chr(c), end="")